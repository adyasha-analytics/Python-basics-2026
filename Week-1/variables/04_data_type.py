# String
name = "adyasha"
print("name value:", name)
print("name type:", type(name))

# Integer
age = 18
print("age value:", age)
print("age type:", type(age))

# Float
price = 25.99
print("price value:", price)
print("price type:", type(price))

# Boolean
is_adult = True
print("is_adult value:", is_adult)
print("is_adult type:", type(is_adult))

# NoneType
result = None
print("result value:", result)
print("result type:", type(result))

# Different ways to define strings
single_quote = 'adyasha'
double_quote = "adyasha"
triple_quote = '''adyasha'''

print(single_quote)
print(double_quote)
print(triple_quote)

