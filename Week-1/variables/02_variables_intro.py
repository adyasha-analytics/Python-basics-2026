# A variable stores data in memory
name = "Adyasha"      # string
age = 18              # integer
price = 25.99         # float

# Printing variables
print(name)
print(age)
print(price)

# Printing text with variables
print("My name is:", name)
print("My age is:", age)

# Variable assignment
age2 = age
print(age2)

# --------------------------------------------
# Important notes:
# - Variables can be reused
# - Python decides data type automatically
# - Strings must be inside quotes
# --------------------------------------------

print("name")   # prints text: name
print(name)     # prints value stored in variable
