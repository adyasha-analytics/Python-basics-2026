#arithmetic operator

a = 10
b = 3

print(a + b) #13
print(a - b) #7
print(a * b) #30
print(a / b) #3.3333333333......
print(a // b) #3
print(a % b) #1

print("10" + "3") #103
print("10" * 3) #101010

#comparision operator

print(a == b) #False
print(a != b) #True
print(a > b) #True
print(a < b) #False

print("10" == 10) #False (different types)