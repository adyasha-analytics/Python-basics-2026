# Loops are used to repeat instructions

#While loop

count = 1
while count <= 5 :
    print("hello")
    count += 1

i = 1 # here i is call iterator
while i <= 100000 :
    print("apnacollage", i)
    i += 1

#print number from 1 to 5

j = 1
while j <= 5:
    print(j)
    j += 1

print("loop ended")

# print number from 5 to 1

k = 5
